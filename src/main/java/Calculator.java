public class Calculator {

     public int sumNumbers(int first, int second) {
         int sumNumbers = first + second;
         return sumNumbers;
     }
    public int subtractNumbers(int first, int second) {
        int subtractNumbers = first - second;
        return subtractNumbers;
    }
    public int multiplyNumbers(int first, int second) {
        int multiplyNumbers = first * second;
        return multiplyNumbers;
    }
    public int divideNumbers(int first, int second) {
        int divideNumbers = first / second;
        return divideNumbers;
    }

}
